int min(int m [], int index){

    int bestEle = m[0];
    int bestIndex = 0;

    #pragma omp parallel for
    for(int i = 0; i < index; i++){
        if(m[i] < bestEle){
            bestEle = m[i];
            bestIndex = i;
        }
    }

    return bestEle;

}

